    private void llenar(){
        negocio neg = new negocio();
        Iterator it = neg.getVentas().iterator();
        while (it.hasNext()) {
            Ventas vta = (Ventas) it.next();
            this.jTextArea1.append("Rut: " + vta.getRut_vta() + "-" + vta.getDigito_vta() + "\n"
                    + "Fecha: " + vta.getFecha_vta() + "\n"
                    + "Monto: " + vta.getMonto_vta() + "\n"
                    + "NÃºmero de Cuotas: " + vta.getNumCuota_vta() + "\n"
                    + "Valor de cuotas: " + vta.getValorCuota_vta() + "\n"
                    + "Interes cuotas: " + vta.getInteresCuota_vta() + "\n"
                    + "Tipo de documento: " + vta.getTipoDoc_vta() + "\n"
                    + "Descripcion de la venta: " + vta.getDescrip_vta() + "\n"
                    + "Vendedor: " + vta.getVendedor_vta() +"\n"
                    + "Sucursal: " + vta.getSucursal_vta() +"\n"
                    + "\n=====================\n");
        }
    }
