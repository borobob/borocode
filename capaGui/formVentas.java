    private void cbxCuotasItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cbxCuotasItemStateChanged
        // TODO add your handling code here:
        this.txtInteres.setText("0");
        double montoFinal;
        int mf;
        this.txtMontoFinal.setText(this.txtMonto.getText());
        if (this.cbxCuotas.getSelectedIndex() == 4
                || this.cbxCuotas.getSelectedIndex() == 5
                || this.cbxCuotas.getSelectedIndex() == 6) {
            montoFinal = Integer.parseInt(this.txtMonto.getText())
                    + (Integer.parseInt(this.txtMonto.getText()) * 0.2);
            mf = (int) montoFinal;
            this.txtMontoFinal.setText(mf + "");
            this.txtInteres.setText("2");
        } else {
            if (this.cbxCuotas.getSelectedIndex() == 7
                    || this.cbxCuotas.getSelectedIndex() == 8
                    || this.cbxCuotas.getSelectedIndex() == 9
                    || this.cbxCuotas.getSelectedIndex() == 10
                    || this.cbxCuotas.getSelectedIndex() == 11
                    || this.cbxCuotas.getSelectedIndex() == 12) {
                montoFinal = Integer.parseInt(this.txtMonto.getText())
                        + (Integer.parseInt(this.txtMonto.getText()) * 0.35);
                mf = (int) montoFinal;
                this.txtMontoFinal.setText(mf + "");
                this.txtInteres.setText("0.35");

            }

        }
        if (this.cbxCuotas.getSelectedIndex() != 0) {
            double valorCuota = Double.parseDouble(this.txtMontoFinal.getText())
                    / this.cbxCuotas.getSelectedIndex();
            int vc = (int) valorCuota;
            this.txtValor.setText(vc + "");
        }






    }//GEN-LAST:event_cbxCuotasItemStateChanged

