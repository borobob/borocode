    private void btnAgregarVtaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarVtaActionPerformed

        //TODO errores de campo vacio
        if (this.txtRut.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null,
                    "Error debe ingresar el rut",
                    "Error de ingreso",
                    JOptionPane.ERROR_MESSAGE);
            this.txtRut.requestFocus();
            return;

        }
        if (this.txtTipMov.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null,
                    "Error debe ingresar el tipo de movimiento",
                    "Error de ingreso",
                    JOptionPane.ERROR_MESSAGE);
            this.txtTipMov.requestFocus();
            return;

        }

        if (this.txtMonto.getText().trim().isEmpty() || this.txtMonto.getText().trim().equalsIgnoreCase("0")) {
            JOptionPane.showMessageDialog(null,
                    "Error debe ingresar el monto",
                    "Error de ingreso",
                    JOptionPane.ERROR_MESSAGE);
            this.txtMonto.requestFocus();
            return;

        }
        if (this.cbxCuotas.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(null,
                    "Error debe seleccionar numero de cuotas",
                    "Error de ingreso",
                    JOptionPane.ERROR_MESSAGE);
            this.cbxCuotas.requestFocus();
            return;

        }
        if (this.txtNumDoc.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null,
                    "Error debe ingresar el Nº de documento",
                    "Error de ingreso",
                    JOptionPane.ERROR_MESSAGE);
            this.txtNumDoc.requestFocus();
            return;

        }
        if (this.cbxTipoDoc.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(null,
                    "Error debe seleccionar tipo de documento",
                    "Error de ingreso",
                    JOptionPane.ERROR_MESSAGE);
            this.cbxTipoDoc.requestFocus();
            return;

        }
        if (this.txtDescrip.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null,
                    "Error debe ingresar una descripción",
                    "Error de ingreso",
                    JOptionPane.ERROR_MESSAGE);
            this.txtDescrip.requestFocus();
            return;

        }
        if (this.cbxRutVendedor.getSelectedIndex()==0) {
            JOptionPane.showMessageDialog(null,
                    "Error debe ingresar un vendedor",
                    "Error de ingreso",
                    JOptionPane.ERROR_MESSAGE);
            this.cbxRutVendedor.requestFocus();
            return;

        }
        if (this.txtSucursal.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null,
                    "Error debe ingresar una sucursal",
                    "Error de ingreso",
                    JOptionPane.ERROR_MESSAGE);
            this.txtSucursal.requestFocus();
            return;
        }

//        if(this.esDeudor()){
//            return;
//        }
        negocio neg = new negocio();
        // Consulto cupo
        double cupoCliente = neg.getCupo(this.txtRut.getText());
        double pendiente = neg.getDebito(this.txtRut.getText()); //***********
        double verificaCupo = cupoCliente -(pendiente + Integer.parseInt(this.txtMonto.getText()));
        if(this.ckbContado.isSelected()==true){verificaCupo=1;}

        if(verificaCupo>=0){
        Ventas vta = new Ventas();
        
        if(neg.existeDocumento(Integer.parseInt(this.txtNumDoc.getText()))==true){

        vta.setRut_vta(this.txtRut.getText().trim());
        vta.setDigito_vta(this.txtDigito.getText().trim());
        vta.setTipoMov_vta(Integer.parseInt(this.txtTipMov.getText().trim()));
        vta.setFecha_vta(Calendar.getInstance().getTime());
        vta.setMonto_vta(Integer.parseInt(this.txtMontoFinal.getText().trim()));
        vta.setNumCuota_vta(this.cbxCuotas.getSelectedIndex());
        vta.setValorCuota_vta(Integer.parseInt(this.txtValor.getText().trim()));
        vta.setInteresCuota_vta(Double.parseDouble(this.txtInteres.getText().trim()));
        vta.setDocu_vta(Integer.parseInt(this.txtNumDoc.getText().trim()));
        vta.setTipoDoc_vta((String) this.cbxTipoDoc.getSelectedItem());
        vta.setDescrip_vta(this.txtDescrip.getText().trim());
        vta.setVendedor_vta((String)this.cbxRutVendedor.getSelectedItem());
        vta.setSucursal_vta(this.txtSucursal.getText().trim());
        if(this.ckbContado.isSelected()==false){}
        String nombre = this.txtNombre.getText();
        neg.addVentas(vta,this.cbxCuotas.getSelectedIndex(),nombre);
        System.out.println(vta);
        String rutVendedor=(String)this.cbxRutVendedor.getSelectedItem();
        JOptionPane.showMessageDialog(null, "Se ingreso correctamente la venta\n" +
        "Vendedor: "+neg.getNomVendedor(rutVendedor));
        double ventasVend = neg.getVentasVendedor(rutVendedor);
        double nuevoTotalVentas = ventasVend + Integer.parseInt(this.txtMontoFinal.getText().trim());
        neg.editVentasVend(rutVendedor,nuevoTotalVentas);

        this.btnLimpiarActionPerformed(evt);
        }else{JOptionPane.showMessageDialog(null, "Numero Documento ya  ingresado");}
        }else{JOptionPane.showMessageDialog(null, "La venta supera el cupo del cliente");}
    }//GEN-LAST:event_btnAgregarVtaActionPerformed

